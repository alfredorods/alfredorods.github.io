$(".portafolio-link").on("click",function(){
    const name = $(this).data("item");
    const project = data_projects.find(function(data){
        if(data.project==name) return data;
    });
    $('#project-description').modal('show');
    $('#title-project').html(project.title);
    $('#role-project').html(project.role);
    $('#year-project').html(project.year);
    $('#status-project').html(project.status);
    $('#platform-project').html(project.platform);
    $('#techs-project').html(project.technologies);
    $('#integrations-project').html(project.integrations);
    $('#description-project').html(project.description);
    $('#links-project').html("");
    $('#lightgallery').html("");
    let links = "";
    links+="<ul>"
    for(let i=0; i<project.links.length; i++) {
        links+="<li><a href='"+project.links[i].link+"' target='_blank'>"+project.links[i].label+"</a></li>";
    }
    links+="</ul>"
    $('#links-project').html(links);

    let images = "";
    for(let i=0; i<project.screenshots.length; i++) {
        images+="<a href="+project.screenshots[i]+" target='_blank'><img class='gallery-item' src='"+project.screenshots[i]+"'/></a>";
    }

    $('#lightgallery').html(images);

    lightGallery(document.getElementById('lightgallery'), {
        plugins: [lgZoom, lgThumbnail]
    });

    $("#lightgallery > a").on("click",function(){
        $('#project-description').modal('hide');
    });



});